/**
 * Reads eval_meta.json + eval_results.csv and writes a Markdown report
 * to $GITHUB_STEP_SUMMARY (GitHub Actions) or stdout (local).
 *
 * Run after `pnpm run eval` or `pnpm run eval:ci`.
 * Usage: node scripts/eval_summary.mjs
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const META_PATH = path.join(ROOT, "eval_meta.json");
const CSV_PATH = path.join(ROOT, "eval_results.csv");

function readCsv(filePath) {
  const lines = fs.readFileSync(filePath, "utf-8").trim().split("\n");
  const headers = lines[0].split(",");
  return lines.slice(1).map((line) => {
    // Handle quoted fields with commas inside
    const values = [];
    let current = "";
    let inQuotes = false;
    for (const ch of line) {
      if (ch === '"') { inQuotes = !inQuotes; continue; }
      if (ch === "," && !inQuotes) { values.push(current); current = ""; continue; }
      current += ch;
    }
    values.push(current);
    return Object.fromEntries(headers.map((h, i) => [h, values[i] ?? ""]));
  });
}

function badge(passed) {
  return passed ? "🟢 PASS" : "🔴 FAIL";
}

function main() {
  if (!fs.existsSync(META_PATH)) {
    console.error("eval_meta.json not found — run pnpm run eval first");
    process.exit(1);
  }

  const meta = JSON.parse(fs.readFileSync(META_PATH, "utf-8"));
  const rows = fs.existsSync(CSV_PATH) ? readCsv(CSV_PATH) : [];

  const statusIcon = meta.passed ? "✅" : "❌";
  const tierLabel = meta.tierFilter === "all" ? "All Tiers" : `Tiers ${meta.tierFilter}`;

  const lines = [
    `## ${statusIcon} VeritasAI Eval — ${tierLabel}`,
    "",
    `**Commit:** \`${meta.commit}\`  **Run at:** ${meta.timestamp}`,
    "",
    "### Quality Gate",
    "",
    "| Check | Result | Score | Threshold |",
    "|-------|--------|-------|-----------|",
  ];

  const t = meta.thresholds;
  const tc = meta.tierCounts;

  if (tc["1"].total > 0) {
    const ok = tc["1"].pass >= t.minTier1;
    lines.push(`| Tier 1 (corpus) | ${badge(ok)} | ${tc["1"].pass}/${tc["1"].total} | ≥${t.minTier1} |`);
  }
  if (tc["2"].total > 0) {
    const ok = tc["2"].pass >= t.minTier2;
    lines.push(`| Tier 2 (tools) | ${badge(ok)} | ${tc["2"].pass}/${tc["2"].total} | ≥${t.minTier2} |`);
  }
  if (tc["3"].total > 0) {
    const ok = tc["3"].pass >= t.minTier3;
    lines.push(`| Tier 3 (all+mem) | ${badge(ok)} | ${tc["3"].pass}/${tc["3"].total} | ≥${t.minTier3} |`);
  }

  const schemaOk = meta.schemaValidity >= t.minSchemaPct;
  lines.push(`| Schema validity | ${badge(schemaOk)} | ${meta.schemaValidity.toFixed(1)}% | ≥${t.minSchemaPct}% |`);

  lines.push(
    "",
    "### Performance",
    "",
    "| Metric | Value |",
    "|--------|-------|",
    `| Questions run | ${meta.totalQuestions} |`,
    `| Pass rate | ${meta.totalPass}/${meta.totalQuestions} (${Math.round(meta.totalPass/meta.totalQuestions*100)}%) |`,
    `| Avg latency | ${meta.avgLatencyMs}ms |`,
    `| Avg citation quality | ${meta.avgCitationQuality.toFixed(2)}/2 |`,
    `| Total API cost | $${meta.totalCostUsd.toFixed(4)} |`,
  );

  if (rows.length > 0) {
    const failed = rows.filter((r) => r.pass === "false");
    if (failed.length > 0) {
      lines.push(
        "",
        "### Failed Questions",
        "",
        "| # | Tier | Question | Error |",
        "|---|------|----------|-------|",
      );
      failed.forEach((r, i) => {
        const q = r.question.slice(0, 60) + (r.question.length > 60 ? "…" : "");
        const err = r.error || r.answer_preview.slice(0, 40) || "—";
        lines.push(`| ${i+1} | ${r.expected_tier} | ${q} | ${err} |`);
      });
    }
  }

  lines.push("", `---`, `*Generated by \`scripts/eval_summary.mjs\`*`);

  const report = lines.join("\n");

  // Write to GitHub Step Summary if in CI
  const summaryFile = process.env.GITHUB_STEP_SUMMARY;
  if (summaryFile) {
    fs.appendFileSync(summaryFile, report + "\n");
    console.log("✅ Summary written to GitHub Step Summary");
  } else {
    console.log(report);
  }

  // Also write a standalone HTML-friendly report
  const reportPath = path.join(ROOT, "eval_report.md");
  fs.writeFileSync(reportPath, report);
  console.log(`Report saved to ${reportPath}`);
}

main();
